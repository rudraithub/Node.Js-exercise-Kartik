 //0414-Write a program to calculate profit or loss.
 let costPrice=1000;
 let sellingPrice=1500

 let profitorloss=sellingPrice-costPrice

 if(profitorloss>0){
    console.log("profit:",profitorloss)
 }
 else if(profitorloss<0){
    console.log("loss",profitorloss)
 }
else{
    console.log("no profit or loss")
}
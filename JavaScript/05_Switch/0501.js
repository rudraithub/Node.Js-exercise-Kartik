// Switch excercise:

// 0501-Write a program to check whether a number is positive, negative or zero using switch case.


let num=23
switch (num) {
    case 23:
        console.log("number is positive")
        break;

        case -23:
            console.log("number is negetive")
            break

    default:
        console.log("number is zero")
        break;
}
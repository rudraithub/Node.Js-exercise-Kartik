
//0504- Write a program to print day of week name using switch case.

let day = 6
switch (day) {
    case 0:
        console.log("sunday")
        break;
    case 1:
        console.log("monday")
        break;

    case 2:
        console.log("tuesday")
        break;

    case 3:
        console.log("wendnesday")
        break;

    case 4:
        console.log("thursday")
        break;

    case 5:
        console.log("friday")
        break;

    case 6:
        console.log("saturday")
        break;

}

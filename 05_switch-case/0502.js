//0502- Write a program to find maximum between two numbers using switch case.

let num = 12
let num1 = 40
switch (num, num1) {
    case 12:
        console.log("number 12 is maximum")
        break;

    case 40:
        console.log("number 40 is maximum")
        break

    default:
        console.log("both number are equal")
        break;
}